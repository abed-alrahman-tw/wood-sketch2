<?php

namespace Database\Seeders;

use App\Models\Category;
use Illuminate\Database\Seeder;
use Illuminate\Support\Str;

class CategorySeeder extends Seeder
{
    public function run(): void
    {
        $categories = [
            'Bespoke Furniture',
            'Built-in Storage',
            'Kitchen Joinery',
            'Outdoor Woodwork',
            'Restoration & Repair',
            'Interior Finishing',
        ];

        foreach ($categories as $name) {
            Category::create([
                'name' => $name,
                'slug' => Str::slug($name),
            ]);
        }
    }
}
